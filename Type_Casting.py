# Convertion one data type to other data type is known...
# Python support wide variety of function for it :
# int(), float() , str() , ord() , hex() , oct() , tuple() , set() , list() , dict() etc.

# Explicit TypeCasting (through human)
a = "1"
b = "b"
print(int(a)+int(b))

# Implicit TypeCasting (through computer)
c = 1.1
d = 6
print(c+d)
# The ans. is in float 
# this conversion is convert through Python "inter printer"
